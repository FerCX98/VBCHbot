import pandas

df=pandas.read_csv("out.csv")
with open('person.txt', 'w') as c:
    for index, row in df.iterrows():
        c.write('sex')
        c.write('(')
        c.write(str(row['personID']))
        c.write(',')
        c.write(str(row['sex']))
        c.write(')')
        c.write('.')
        c.write("\n")
        c.write('marital')
        c.write('(')
        c.write(str(row['personID']))
        c.write(',')
        c.write(str(row['maritalStatus']))
        c.write(')')
        c.write('.')
        c.write("\n")
        c.write('age')
        c.write('(')
        c.write(str(row['personID']))
        c.write(',')
        c.write(str(row['age']))
        c.write(')')
        c.write('.')
        c.write("\n")
        c.write('ethnic')
        c.write('(')
        c.write(str(row['personID']))
        c.write(',')
        c.write(str(row['ethnicGroup']))
        c.write(')')
        c.write('.')
        c.write("\n")
        c.write('industry')
        c.write('(')
        c.write(str(row['personID']))
        c.write(',')
        c.write(str(row['industry']))
        c.write(')')
        c.write('.')
        c.write("\n")
        c.write('firstName')
        c.write('(')
        c.write(str(row['personID']))
        c.write(',')
        c.write('"')
        c.write(str(row['firstName']))
        c.write('"')
        c.write(')')
        c.write('.')
        c.write("\n")
        c.write('lastName')
        c.write('(')
        c.write(str(row['personID']))
        c.write(',')
        c.write('"')
        c.write(str(row['lastName']))
        c.write('"')
        c.write(')')
        c.write('.')
        c.write("\n")